public class ContadorParImpar {
    public static void main(String[] args){
        for(int i = 0; i<= 20; i++){
            System.out.println(i);
            if(i % 2 == 0){
                System.out.println("O número é par!!");

            }
            else{ 
                System.out.println("O número é ímpar!!");
            }
        }
    }
    
}
